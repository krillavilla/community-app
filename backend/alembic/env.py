"""
Alembic environment configuration.

Loads SQLAlchemy models and manages database migrations.
"""
from logging.config import fileConfig
import sys
from pathlib import Path

from sqlalchemy import engine_from_config
from sqlalchemy import pool

from alembic import context

# Add app directory to path
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

# Import app configuration and models
from app.core.config import settings
from app.core.database import Base

# Import all models so Alembic can detect them
from app.models import (
    User, GuideProfile, GuideApplication, TrustVerificationApplication,
    Garden, Habit, HabitLog,
    FlourishPost, Comment, Reaction,
    Connection, Message, MentorshipRequest,
    NourishmentItem,
    SunlightPost, SunlightReaction,
    Project, ProjectMember, ProjectDiscussion,
    SupportRequest, SupportResponse,
    FellowshipGroup, GroupMember, GroupEvent, EventRSVP,
    Report
)

# Alembic Config object
config = context.config

# Interpret the config file for Python logging
if config.config_file_name is not None:
    fileConfig(config.config_file_name)

# Set target metadata for autogenerate
target_metadata = Base.metadata

# Override sqlalchemy.url from settings
config.set_main_option("sqlalchemy.url", settings.DATABASE_URL)


def run_migrations_offline() -> None:
    """
    Run migrations in 'offline' mode.
    
    This configures the context with just a URL and not an Engine.
    Calls to context.execute() emit the given string to the script output.
    """
    url = config.get_main_option("sqlalchemy.url")
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
        compare_type=True,
        compare_server_default=True,
    )

    with context.begin_transaction():
        context.run_migrations()


def run_migrations_online() -> None:
    """
    Run migrations in 'online' mode.
    
    Creates an Engine and associates a connection with the context.
    """
    connectable = engine_from_config(
        config.get_section(config.config_ini_section, {}),
        prefix="sqlalchemy.",
        poolclass=pool.NullPool,
    )

    with connectable.connect() as connection:
        context.configure(
            connection=connection,
            target_metadata=target_metadata,
            compare_type=True,
            compare_server_default=True,
        )

        with context.begin_transaction():
            context.run_migrations()


if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()
